// Function Declaration

function sayHello(){
    console.log(`Hello!`)
}

sayHello()

function sayHelloTo(name){
    console.log(`Hello ${name}`)
}

sayHelloTo(`Victor`)

function returnHi(){
    return `Hi!`
}

let greeting = returnHi()
console.log(greeting)

function returnHiTo(name){
    return `Hi ${name}!`
}

console.log(returnHiTo(`John`))
